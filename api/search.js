module.exports = async function handler(req, res) {
  res.setHeader('Access-Control-Allow-Origin', '*');
  res.setHeader('Access-Control-Allow-Methods', 'POST, OPTIONS');
  res.setHeader('Access-Control-Allow-Headers', 'Content-Type');
  if (req.method === 'OPTIONS') return res.status(200).end();
  if (req.method !== 'POST') return res.status(405).json({ error: 'Method not allowed' });

  const { word, ageLabel, meaning } = req.body;
  if (!word || !ageLabel) return res.status(400).json({ error: '단어와 나이를 입력해주세요.' });

  const meaningClause = meaning
    ? `단, "${word}" 중에서 "${meaning}" 의미로만 설명해주세요.`
    : '';

  const ageGuide = {
    '3~4살': `
■ 이 아이가 아는 단어 수준
  - 가족: 엄마, 아빠, 할머니, 아기
  - 음식: 밥, 빵, 사과, 우유, 과자
  - 동물: 강아지, 고양이, 토끼, 새
  - 행동: 먹다, 자다, 울다, 웃다, 뛰다, 안다
  - 상태: 크다, 작다, 뜨겁다, 차갑다, 예쁘다

■ 설명 방법
  - 문장 2개로만 설명. 한 문장은 반드시 8단어 이하.
  - "~해요", "~이에요" 말투만 사용
  - 반드시 아이 몸이나 일상에서 느낄 수 있는 것으로 비유

■ 절대 사용하면 안 되는 것
  - 한자어: 시간, 공간, 관찰, 자연, 계절, 환경, 지평선, 에너지
  - 추상어: 개념, 의미, 상태, 현상, 과정, 원리
  - 어려운 동사: 취하다, 마련하다, 지니다, 발생하다
  - 긴 문장, 접속사 남발 (그러므로, 따라서, 반면에)

■ 나쁜 예시 vs 좋은 예시
  ❌ "밤은 지구가 자전하며 태양의 반대편에 위치할 때 발생하는 현상이에요. 기온이 낮아지고 생물들이 휴식을 취하는 시간이에요."
  ✅ "밤은 해가 자러 가서 하늘이 까매지는 때예요. 엄마 아빠랑 이불 속에서 쿨쿨 자는 시간이에요."

  ❌ "사랑은 누군가를 매우 소중히 여기고 아끼는 감정이에요. 상대방의 행복을 바라며 희생하는 마음이에요."
  ✅ "사랑은 엄마가 아플 때 꼭 안아주고 싶은 마음이에요. 강아지를 보면 막 쓰다듬어 주고 싶은 그 느낌이에요."`,

    '5~6살': `
■ 이 아이가 아는 단어 수준
  - 유치원 교재에 나오는 단어들
  - 가족, 친구, 유치원, 놀이터, 블록, 그림책
  - 감정: 기쁘다, 슬프다, 무섭다, 신나다, 부끄럽다
  - 자연: 해, 달, 비, 눈, 꽃, 나무, 바람
  - 숫자, 색깔, 모양 등 기초 개념

■ 설명 방법
  - 문장 2~3개. 한 문장 12단어 이하.
  - "~해요", "~이에요" 말투
  - 경험으로 연결: "유치원에서", "집에서", "놀이터에서"
  - 어려운 단어가 꼭 필요하면 바로 뒤에 쉽게 풀어서 설명

■ 절대 사용하면 안 되는 것
  - 한자어: 지평선, 수평선, 기온, 일과, 휴식, 취하다
  - 과학/학술 용어: 자전, 공전, 광합성, 에너지
  - 어른 말투: "~함으로써", "~에 따라", "~을 통해"

■ 나쁜 예시 vs 좋은 예시
  ❌ "밤은 지구의 자전으로 태양광이 차단되는 현상이에요. 기온이 저하되고 생물이 휴식을 취하는 시간이에요."
  ✅ "밤은 해님이 지고 하늘이 어두워지는 시간이에요. 별이 반짝반짝 뜨고 달님도 나타나요. 우리도 밤이 되면 잠자리에 들어 쉬어야 해요."

  ❌ "용기는 두려움을 극복하고 어려운 상황에 맞서는 심리적 힘이에요."
  ✅ "용기는 무서워도 한번 해보려는 마음이에요. 처음 미끄럼틀을 탈 때 무서웠지만 타봤잖아요, 그게 바로 용기예요."`,

    '7~8살': `
■ 이 아이가 아는 단어 수준
  - 초등 1~2학년 교과서 어휘
  - 학교, 선생님, 공부, 숙제, 친구, 운동장
  - 사회: 마을, 시장, 병원, 소방서
  - 자연: 계절, 날씨, 동물, 식물의 기초 지식
  - 감정과 생각을 표현하는 단어

■ 설명 방법
  - 문장 2~3개. 명확한 정의 + 구체적인 예시.
  - 비유나 이야기로 연결하면 좋음
  - 새로운 개념은 이미 아는 것과 연결해서 설명

■ 피해야 할 것
  - 너무 어려운 학술 용어 (자전축, 일주기, 광합성 메커니즘)
  - 설명 없이 쓰는 한자어

■ 나쁜 예시 vs 좋은 예시
  ❌ "밤은 지구의 자전축 기울기로 인해 태양 복사에너지가 차단되는 일주기 현상이에요."
  ✅ "밤은 지구가 해를 등지고 있어서 어두워지는 시간이에요. 낮 동안 열심히 놀고 공부한 몸이 쉬는 소중한 시간이죠. 동물들도 밤이 되면 잠을 자며 에너지를 [충전]해요."

  ❌ "책임은 자신의 행위에 대한 결과를 감수하는 도덕적 의무예요."
  ✅ "책임은 내가 맡은 일을 끝까지 해내는 거예요. 화단 물주기를 맡았으면 비가 와도 안 와도 매일 확인하는 것처럼요."`,

    '9~10살': `
■ 이 아이가 아는 단어 수준
  - 초등 3~4학년 교과서 어휘
  - 과학: 자전, 공전, 에너지, 생태계 기초
  - 사회: 문화, 역사, 경제 기초 개념
  - 추상 개념을 어느 정도 이해함
  - 원인과 결과, 비교와 대조 이해 가능

■ 설명 방법
  - 문장 3개 내외. 정의 → 원리 → 생활 연결 구조.
  - 과학적 원리나 배경지식 간단히 포함 가능
  - 실생활과 연결되는 흥미로운 사실 추가 가능

■ 피해야 할 것
  - 전문가 수준 학술 용어 (중학생 이상 수준)
  - 너무 길고 복잡한 문장

■ 나쁜 예시 vs 좋은 예시
  ❌ "밤은 지구의 자전으로 인한 일주기 리듬에서 태양 복사에너지가 차단되어 기온이 하강하고 광합성이 중단되는 시간대입니다."
  ✅ "밤은 지구의 자전으로 태양 빛이 닿지 않는 시간대예요. 낮에 받은 열이 식으면서 기온이 낮아지고, 식물은 광합성을 멈추고 산소 대신 이산화탄소를 내보내요. 사람과 동물은 밤 동안 잠을 자며 낮 동안 쌓인 피로를 회복해요."

  ❌ "민주주의는 국민 주권 원리에 기반한 정치 체제로서 삼권분립을 핵심 기제로 작동합니다."
  ✅ "민주주의는 나라의 중요한 일을 국민이 함께 결정하는 방식이에요. 반장 선거처럼 모두가 투표로 대표를 뽑고, 그 대표들이 법을 만들고 나라를 운영해요. 한 사람이 마음대로 결정하지 않고 여러 사람이 의견을 나눠서 정하는 게 핵심이에요."`
  };

  const prompt = `당신은 새싹사전입니다. 아이 눈높이에 맞게 단어를 정확하고 쉽게 설명해주는 어린이 전용 사전이에요.
"${word}"라는 단어를 ${ageLabel} 아이에게 설명해주세요. ${meaningClause}

━━━ ${ageLabel} 설명 규칙 (반드시 준수) ━━━
${ageGuide[ageLabel] || ageGuide['5~6살']}

━━━ 최종 검토 (작성 후 반드시 확인) ━━━
✔ definition의 모든 단어가 위 아이 수준에 맞는가?
✔ 한 문장이 지정한 단어 수를 넘지 않는가?
✔ 금지 단어가 포함되지 않았는가?
✔ 부모가 이 설명을 아이에게 읽어줄 때 자연스러운가?
✔ 아이가 들었을 때 바로 이해하고 고개를 끄덕일 수 있는가?

반드시 아래 JSON 형식으로만 응답하세요. 다른 말은 절대 하지 마세요:
{
  "emoji": "단어를 가장 잘 나타내는 이모지 1개",
  "reading": "발음이 표기와 다를 때만 작성 (예: 외래어, 한자어). 같으면 빈 문자열.",
  "definition": "위 규칙을 100% 준수한 설명. 아이가 모를 수 있는 단어는 [단어] 형식으로 표시.",
  "example": "위 규칙을 준수한 자연스러운 일상 예문 1개. 아이가 모를 수 있는 단어는 [단어] 형식으로 표시.",
  "synonyms": ["나이에 맞는 비슷한 말 2~3개"],
  "antonyms": ["나이에 맞는 반대 말 1~2개"],
  "related": ["나이에 맞는 연관 단어 3개"],
  "tags": ["관련 개념 태그 2~3개"]
}`;

  try {
    const response = await fetch('https://api.anthropic.com/v1/messages', {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        'x-api-key': process.env.ANTHROPIC_API_KEY,
        'anthropic-version': '2023-06-01'
      },
      body: JSON.stringify({
        model: 'claude-sonnet-4-5',
        max_tokens: 1000,
        messages: [{ role: 'user', content: prompt }]
      })
    });
    if (!response.ok) {
      const errBody = await response.text();
      throw new Error(`API 오류: ${response.status} / ${errBody}`);
    }
    const data = await response.json();
    const raw = data.content.map(i => i.text || '').join('');
    const clean = raw.replace(/```json|```/g, '').trim();
    return res.status(200).json(JSON.parse(clean));
  } catch (err) {
    console.error(err);
    return res.status(500).json({ error: err.message });
  }
};
